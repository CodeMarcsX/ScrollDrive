// ================================
// ANIMATION OF THE FINAL TEXT
// ================================

export function initFinalText() {
    gsap.registerPlugin(ScrollTrigger); // Registers the ScrollTrigger plugin in GSAP

    const split = new SplitType(".outro p", { // Splits the paragraph text into animatable parts
        types: "lines"                        // Breaks the text into visual lines
    });
    
    gsap.to(".outro p .line", {               // Animates all lines generated by SplitType
        backgroundPositionX: "0%",            // Moves the gradient to reveal the text color
        ease: "none",                         // Keeps the animation linear (no acceleration)
        stagger: 1,                           // Animates one line after another
    
        scrollTrigger: {                      // Scroll-based animation configuration
            trigger: ".outro",                // Element that controls the animation start and end
            start: "top 30%",                 // Starts when the top of the section reaches 30% of the screen
            end: "bottom 100%",               // Ends when the bottom of the section reaches the bottom of the screen
            scrub: true,                      // Synchronizes the animation directly with the scroll
        }
    });

}
